function setLanguage(lang) {
    localStorage.setItem('preferred_lang', lang);
    const translation = i18n[lang];

    // 1. Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translation[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation[key];
            } else {
                el.innerText = translation[key];
            }
        }
    });

    // 2. Update Layout Direction
    document.documentElement.dir = translation.direction;
    document.documentElement.lang = lang;
    
    // 3. Update Font Family for Urdu
    if (lang === 'ur') {
        document.body.style.fontFamily = "'Noto Nastaliq Urdu', serif";
    } else {
        document.body.style.fontFamily = "'Inter', sans-serif";
    }

    // 4. Update the AI Chat instructions (if the function exists)
    if (typeof updateAISystemPrompt === 'function') {
        updateAISystemPrompt(lang);
    }
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferred_lang') || 'en';
    setLanguage(savedLang);
    
    // Update active state of language buttons if any
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('onclick').includes(savedLang)) {
            btn.classList.add('bg-gold', 'text-primary');
            btn.classList.remove('text-white/70');
        } else {
            btn.classList.remove('bg-gold', 'text-primary');
            btn.classList.add('text-white/70');
        }
    });
});
